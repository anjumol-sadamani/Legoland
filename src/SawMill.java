import java.util.List;

public class SawMill {
    private int noOfTrunks;
    private List<Integer> trunks;

    public int getNoOfTrunks() {
        return noOfTrunks;
    }

    public void setNoOfTrunks(int noOfTrunks) {
        this.noOfTrunks = noOfTrunks;
    }

    public List<Integer> getTrunks() {
        return trunks;
    }

    public void setTrunks(List<Integer> trunks) {
        this.trunks = trunks;
    }
}
